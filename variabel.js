//mendeklarasikan variabel dengan var
var x = 5;
var y = 7;
var z = x + y;
console.log(z); //hasilnya 12

//mendeklarasi variabel dengan let
let a = 5;
let b = 6;
let c = a + b;
console.log(c);

//mendeklarasi variabel dengan const
const PI = 3.14159265359;
console.log(PI);

//Mendeklarasikan variabel dengan nothing
f = 5;
z = 6;
h = f + z;
console.log(h);