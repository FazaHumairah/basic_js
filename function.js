// Function dengan parameter
function tambah(a, b) {
    return a + b;
  }
  // Memanggil function
  var hasil = tambah(3, 4);
  console.log(hasil); // Output: 7
  // Function tanpa parameter
  function sayHello() {
    console.log("Hello!");
  }
  // Memanggil function
  sayHello(); // Output: Hello!
  